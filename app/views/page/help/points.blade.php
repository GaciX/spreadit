<h2>Point System</h2>
<p>
points are awarded by adding posts, and by receiving upvotes<br>
voting costs a point, use your points for good<br>
once a user has less than one point he is no longer able to interact with the site<br>
good posts will flow to the top using an algorithm that takes into account age of post and the amount of points a post has accumulated<br>
users are able to vote on spreadits which will either bring them to the top list or keep them off of the topbar<br>
users can also vote on comments<br>
comment votes do not affect sorting at this time<br>
